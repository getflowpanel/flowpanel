/**
 * Processes a ResourceDescriptor with ModelMetadata to produce a ResolvedResource.
 *
 * The generic `TRow` exists purely to accept typed descriptors from user code;
 * internally everything is erased to `Row` because ModelMetadata is runtime data.
 */
export function resolveResource<TRow = Row>(
  key: string,
  descriptor: ResourceDescriptor<TRow>,
  metadata: ModelMetadata,
): ResolvedResource {
  const opts: ResourceOptions<TRow> = descriptor.opts ?? {};

  // ---- Label ----------------------------------------------------------------
  const modelName = metadata.name;
  let label: string;
  let labelPlural: string;

  if (opts.label) {
    label = opts.label;
    labelPlural = opts.labelPlural ?? pluralize(opts.label);
  } else {
    label = titleCase(modelName);
    labelPlural = opts.labelPlural ?? pluralize(label);
  }

  // ---- Columns --------------------------------------------------------------
  let columns: ResolvedColumn[];
  if (Array.isArray(opts.columns)) {
    // Shorthand: array of PathFns
    columns = resolveShorthandColumns(opts.columns as PathFn<TRow>[]);
  } else if (typeof opts.columns === "function") {
    columns = opts.columns(createColumnBuilder<TRow>());
  } else {
    columns = autoColumns(metadata);
  }

  // ---- Filters --------------------------------------------------------------
  let filters: ResolvedFilter[];
  if (Array.isArray(opts.filters)) {
    filters = resolveShorthandFilters(opts.filters as PathFn<TRow>[]);
  } else if (typeof opts.filters === "function") {
    filters = opts.filters(createFilterBuilder<TRow>());
  } else {
    filters = autoFilters(metadata);
  }

  // ---- Search ---------------------------------------------------------------
  let searchFields: string[];
  if (opts.searchFields) {
    searchFields = resolvePathStrings(opts.searchFields as PathFn<TRow>[]);
  } else {
    searchFields = autoSearchFields(metadata);
  }

  // ---- Actions --------------------------------------------------------------
  let actions: ResolvedAction[] = [];
  if (opts.actions) {
    const raw = opts.actions(createActionBuilder<TRow>());

    // Support both Record<string, ResolvedAction> (keyed) and array forms
    if (Array.isArray(raw)) {
      actions = raw;
    } else {
      actions = Object.entries(raw).map(([actionId, action]) => ({
        ...action,
        id: actionId,
      }));
    }

    // Validate reserved IDs
    for (const action of actions) {
      if (RESERVED_ACTION_IDS.has(action.id)) {
        throw new Error(
          `Action id "${action.id}" is reserved. Reserved IDs are: ${[...RESERVED_ACTION_IDS].join(", ")}. Use a custom action id.`,
        );
      }
    }
  }

  // ---- Include inference ----------------------------------------------------
  const columnPaths = columns.filter((c) => c.path !== null).map((c) => c.path as string);
  const filterPaths = filters.map((f) => f.path);
  const allPaths = [...columnPaths, ...filterPaths, ...searchFields];

  const inferredInclude = inferIncludeFromPaths(allPaths);
  const include = mergeIncludes(inferredInclude, opts.include);
  const finalInclude = Object.keys(include).length > 0 ? include : undefined;

  // ---- Default sort ---------------------------------------------------------
  const defaultSort = opts.defaultSort ?? { field: metadata.primaryKey, dir: "desc" as const };

  // ---- Server-side maps -----------------------------------------------------
  const _handlers: Record<string, (row: Row, ctx: unknown) => Promise<unknown>> = {};
  const _computes: Record<string, (row: Row) => unknown> = {};
  const _whens: Record<string, (row: Row) => boolean> = {};

  for (const action of actions) {
    _handlers[action.id] = action.handler;
    if (action.when) {
      _whens[action.id] = action.when;
    }
  }

  for (const col of columns) {
    if (col.compute) {
      _computes[col.id] = col.compute;
    }
  }

  return {
    id: key,
    modelName,
    primaryKey: metadata.primaryKey,
    label,
    labelPlural,
    icon: opts.icon ?? "file-text",
    path: opts.path ?? key,
    defaultSort,
    defaultPageSize: opts.defaultPageSize ?? 50,
    searchFields,
    columns,
    filters,
    actions,
    access: opts.access ?? {},
    fieldAccess: opts.fieldAccess ?? [],
    include: finalInclude,
    readOnly: opts.readOnly,
    _handlers,
    _computes,
    _whens,
  };
}
