// Label helpers
// ---------------------------------------------------------------------------

/**
 * Pluralize an English word using common rules. Handles the main cases admin
 * UIs encounter; for irregular plurals, pass `labelPlural` explicitly.
 *
 * Rules:
 * - consonant + y → ies (Company → Companies), but vowel + y → +s (Day → Days)
 * - ends in s/x/z/ch/sh → +es (Status → Statuses, Box → Boxes, Match → Matches)
 * - consonant + o → +es (Hero → Heroes), but vowel + o → +s (Studio → Studios)
 * - ends in f/fe → ves (Leaf → Leaves, Knife → Knives)
 * - otherwise → +s
 */
function pluralize(word: string): string {
  if (!word) return word;
  const lower = word.toLowerCase();

  // f / fe → ves (Leaf → Leaves, Knife → Knives)
  if (lower.endsWith("fe")) return word.slice(0, -2) + "ves";
  if (lower.endsWith("f") && !lower.endsWith("ff")) return word.slice(0, -1) + "ves";

  // consonant + y → ies (Category → Categories)
  if (lower.endsWith("y") && lower.length >= 2 && !isVowel(lower[lower.length - 2]!)) {
    return word.slice(0, -1) + "ies";
  }

  // s / x / z / ch / sh → es
  if (/(s|x|z|ch|sh)$/i.test(lower)) {
    return word + "es";
  }

  // consonant + o → oes (Hero → Heroes); vowel + o → os (Studio → Studios)
  if (lower.endsWith("o") && lower.length >= 2 && !isVowel(lower[lower.length - 2]!)) {
    return word + "es";
  }

  return word + "s";
}

function isVowel(ch: string): boolean {
  return ch === "a" || ch === "e" || ch === "i" || ch === "o" || ch === "u";
}

// ---------------------------------------------------------------------------
// Auto-column / filter / search helpers
// ---------------------------------------------------------------------------

function autoColumns(metadata: ModelMetadata): ResolvedColumn[] {
  const c = createColumnBuilder<Row>();
  return metadata.fields
    .filter(
      (f) =>
        !f.isAutoGenerated && f.kind !== "relation" && (f.kind === "scalar" || f.kind === "enum"),
    )
    .map((f) => c.field((p: PathProxy<Row>) => p[f.name] as unknown as never));
}

function autoFilters(metadata: ModelMetadata): ResolvedFilter[] {
  const f = createFilterBuilder<Row>();
  return metadata.fields
    .filter((field) => field.kind === "enum" || field.type === "boolean")
    .map((field) => f.filter((p: PathProxy<Row>) => p[field.name] as unknown as never));
}

function autoSearchFields(metadata: ModelMetadata): string[] {
  return metadata.fields
    .filter((f) => f.kind === "scalar" && f.type === "string")
    .map((f) => f.name);
}

// ---------------------------------------------------------------------------
// inferIncludeFromPaths
// ---------------------------------------------------------------------------

/**
 * Converts dot-paths to a Prisma-style include tree.
 *
 * Examples:
 *   ["email"]                  → {}
 *   ["user.email"]             → { user: true }
 *   ["user.subscription.planId"] → { user: { include: { subscription: true } } }
 *   ["_count.posts"]           → {}
 */
export function inferIncludeFromPaths(paths: string[]): Record<string, unknown> {
  const result: Record<string, unknown> = {};

  for (const path of paths) {
    const segments = path.split(".");
    if (segments.length < 2) {
      // Scalar — no include needed
      continue;
    }

    const [first, ...rest] = segments;

    // Skip _count
    if (first === "_count") {
      continue;
    }

    // Single-level relation: ["user", "email"] → user: true
    if (rest.length === 1) {
      // Only set to true if not already a nested include (manual wins via mergeIncludes)
      if (result[first] === undefined) {
        result[first] = true;
      }
      // If it's already an object include, keep it (more specific)
      continue;
    }

    // Nested relation: ["user", "subscription", "planId"]
    // We need user: { include: { subscription: true } }
    const relationSegments = rest.slice(0, -1); // all but the leaf field

    // Build or merge nested include
    let current = result;
    let currentKey = first;

    for (let i = 0; i < relationSegments.length; i++) {
      const relationName = relationSegments[i];

      if (current[currentKey] === undefined || current[currentKey] === true) {
        // Upgrade from true → nested include object
        current[currentKey] = { include: {} };
      }

      const node = current[currentKey] as { include: Record<string, unknown> };
      if (!node.include) {
        node.include = {};
      }

      if (i === relationSegments.length - 1) {
        // Last relation segment — set to true if not already nested
        if (node.include[relationName] === undefined) {
          node.include[relationName] = true;
        }
      } else {
        if (node.include[relationName] === undefined || node.include[relationName] === true) {
          node.include[relationName] = { include: {} };
        }
        current = node.include;
        currentKey = relationName;
      }
    }
  }

  return result;
}

// ---------------------------------------------------------------------------
// mergeIncludes
// ---------------------------------------------------------------------------

/**
 * Deep merges inferred includes with manual includes. Manual wins on conflict.
 */
export function mergeIncludes(
  inferred: Record<string, unknown>,
  manual: Record<string, unknown> | undefined,
): Record<string, unknown> {
  if (!manual) return inferred;

  const result: Record<string, unknown> = { ...inferred };

  for (const [key, manualVal] of Object.entries(manual)) {
    // Manual always wins
    result[key] = manualVal;
  }

  return result;
}

