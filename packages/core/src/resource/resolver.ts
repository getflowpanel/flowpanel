// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const RESERVED_ACTION_IDS = new Set(["list", "read", "create", "update", "delete"]);

// ---------------------------------------------------------------------------
// createResourceDescriptor
// ---------------------------------------------------------------------------

/**
 * Factory for ResourceDescriptor. The first arg is an opaque model reference
 * (Prisma delegate, Drizzle table, or string model name). Second arg is optional
 * ResourceOptions.
 */
export function createResourceDescriptor<TRow = Row>(
  modelRef: unknown,
  opts: ResourceOptions<TRow> = {},
): ResourceDescriptor<TRow> {
  return {
    __brand: "flowpanel.resource",
    modelRef,
    opts,
  };
}

