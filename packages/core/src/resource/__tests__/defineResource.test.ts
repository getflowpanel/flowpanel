import { describe, expect, it, vi } from "vitest";
import { isColumnRef, isComputedColumnInput, makeColumnRef } from "../columnRefs";
import { defineResource } from "../defineResource";
import { createTableProxy } from "../tableProxy";
import type { FieldMetadata, ModelMetadata } from "../types";

/**
 * A synthetic "table type" paired with `userMeta`. In real use TTable comes
 * from a Drizzle table or Prisma delegate; the tests use an explicit shape
 * so `defineResource<UserTable>(userMeta, ...)` types the selector correctly.
 */
type UserTable = {
  id: number;
  email: string;
  plan: "free" | "pro";
  category: { id: number; name: string } | null;
};

const userMeta: ModelMetadata = {
  name: "User",
  tableName: "users",
  primaryKey: "id",
  fields: [
    {
      name: "id",
      type: "int",
      kind: "scalar",
      isRequired: true,
      isList: false,
      isId: true,
      isAutoGenerated: true,
    },
    {
      name: "email",
      type: "string",
      kind: "scalar",
      isRequired: true,
      isList: false,
      isId: false,
      isAutoGenerated: false,
    },
    {
      name: "plan",
      type: "enum",
      kind: "enum",
      isRequired: true,
      isList: false,
      isId: false,
      isAutoGenerated: false,
      enumValues: ["free", "pro"],
    },
    {
      name: "category",
      type: "relation",
      kind: "relation",
      isRequired: false,
      isList: false,
      isId: false,
      isAutoGenerated: false,
      relationModel: "Category",
    },
  ] satisfies FieldMetadata[],
};

describe("ColumnRef", () => {
  it("marks values as ColumnRef via branded symbol", () => {
    const ref = makeColumnRef("email", {
      name: "email",
      type: "string",
      kind: "scalar",
      isRequired: true,
      isList: false,
      isId: false,
      isAutoGenerated: false,
    });
    expect(isColumnRef(ref)).toBe(true);
    expect(isColumnRef({ path: "email" })).toBe(false);
    expect(isColumnRef(null)).toBe(false);
    expect(isColumnRef("email")).toBe(false);
  });

  it("exposes path + metadata verbatim", () => {
    const ref = makeColumnRef("user.email", {
      name: "email",
      type: "string",
      kind: "scalar",
      isRequired: false,
      isList: false,
      isId: false,
      isAutoGenerated: false,
      enumValues: ["a", "b"],
    });
    expect(ref.path).toBe("user.email");
    expect(ref.metadata.type).toBe("string");
    expect(ref.metadata.enumValues).toEqual(["a", "b"]);
  });

  it("is frozen — cannot be mutated by consumers", () => {
    const ref = makeColumnRef("id", {
      name: "id",
      type: "int",
      kind: "scalar",
      isRequired: true,
      isList: false,
      isId: true,
      isAutoGenerated: true,
    });
    expect(Object.isFrozen(ref)).toBe(true);
  });
});

describe("createTableProxy", () => {
  it("returns a ColumnRef for scalar property access", () => {
    const proxy = createTableProxy<UserTable>(userMeta);
    const ref = proxy.email;
    expect(isColumnRef(ref)).toBe(true);
    expect(ref.path).toBe("email");
    expect(ref.metadata.type).toBe("string");
  });

  it("surfaces enum metadata on the ref", () => {
    const proxy = createTableProxy<UserTable>(userMeta);
    expect(proxy.plan.metadata.enumValues).toEqual(["free", "pro"]);
  });

  it("returns a relation ref", () => {
    const proxy = createTableProxy<UserTable>(userMeta);
    const ref = proxy.category;
    expect(ref.metadata.kind).toBe("relation");
    expect(ref.metadata.relationModel).toBe("Category");
  });

  it("throws with a helpful message on unknown column", () => {
    const proxy = createTableProxy<UserTable>(userMeta);
    expect(
      () =>
        // @ts-expect-error — runtime test that unknown column throws
        proxy.nonexistent,
    ).toThrow(/no column "nonexistent".*email/);
  });

  it("caches refs so identity checks are stable", () => {
    const proxy = createTableProxy<UserTable>(userMeta);
    expect(proxy.email).toBe(proxy.email);
  });
});

describe("ComputedColumnInput", () => {
  it("detects a computed column by id + compute", () => {
    expect(
      isComputedColumnInput({
        id: "totalPaid",
        label: "Total",
        compute: () => 0,
      }),
    ).toBe(true);
  });

  it("detects a computeBatch form", () => {
    expect(
      isComputedColumnInput({
        id: "totalPaid",
        computeBatch: () => new Map(),
      }),
    ).toBe(true);
  });

  it("rejects objects without compute/computeBatch", () => {
    expect(isComputedColumnInput({ id: "foo" })).toBe(false);
    expect(isComputedColumnInput({ compute: () => 0 })).toBe(false);
    expect(isComputedColumnInput(null)).toBe(false);
  });
});

