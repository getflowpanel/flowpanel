describe("defineFlowPanel v2 — schema serialization", () => {
  it("serializes resources for client consumption", () => {
    const mockAdapter = createMockResourceAdapter();

    const fp = defineFlowPanel({
      ...baseConfig,
      adapter: { sql: mockSqlExecutor, resource: mockAdapter },
      resources: {
        users: resource("User", {
          label: "Users",
          access: {
            create: ["admin"],
            delete: false,
          },
        }),
      },
    });

    // With admin role
    const adminSchema = fp.getSchema(["admin"]);
    expect(adminSchema.appName).toBe("TestApp");
    expect(adminSchema.resources).toBeDefined();

    const userSer = adminSchema.resources!.users;
    expect(userSer.id).toBe("users");
    expect(userSer.label).toBe("Users");
    expect(userSer.access.create).toBe(true); // admin has access
    expect(userSer.access.delete).toBe(false); // explicitly denied
    expect(userSer.access.list).toBe(true); // default allowed

    // Without admin role
    const guestSchema = fp.getSchema([]);
    const guestUser = guestSchema.resources!.users;
    expect(guestUser.access.create).toBe(false); // no matching role
    expect(guestUser.access.delete).toBe(false);
    expect(guestUser.access.list).toBe(true);
  });

  it("serialized columns strip functions", () => {
    const mockAdapter = createMockResourceAdapter();

    const fp = defineFlowPanel({
      ...baseConfig,
      adapter: { sql: mockSqlExecutor, resource: mockAdapter },
      resources: {
        users: resource("User", {
          columns: (c) => [
            c.field((p) => p.email),
            c.computed("fullInfo", {
              label: "Full Info",
              compute: (row) => `${row.name} <${row.email}>`,
            }),
          ],
        }),
      },
    });

    const schema = fp.getSchema();
    const userSer = schema.resources!.users;
    expect(userSer.columns).toHaveLength(2);
    expect(userSer.columns[0].id).toBe("email");
    expect(userSer.columns[1].id).toBe("fullInfo");
    expect(userSer.columns[1].type).toBe("computed");
    // No compute function in serialized output
    expect((userSer.columns[1] as Record<string, unknown>).compute).toBeUndefined();
  });
});

