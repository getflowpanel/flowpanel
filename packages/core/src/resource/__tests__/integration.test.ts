describe("defineFlowPanel v2 — backward compatibility", () => {
  it("works with plain SqlExecutor (no resources)", () => {
    const fp = defineFlowPanel({
      ...baseConfig,
      adapter: mockSqlExecutor,
    });

    expect(fp.config.appName).toBe("TestApp");
    expect(fp.resources).toBeUndefined();
    expect(fp.resourceAdapter).toBeUndefined();
    expect(fp.queryBuilder).toBeDefined();
    expect(typeof fp.withRun).toBe("function");
    expect(typeof fp.startReaper).toBe("function");
    expect(typeof fp.getDb).toBe("function");
  });

  it("returns schema with appName and no resources when none configured", () => {
    const fp = defineFlowPanel({
      ...baseConfig,
      adapter: mockSqlExecutor,
    });

    const schema = fp.getSchema();
    expect(schema.appName).toBe("TestApp");
    expect(schema.resources).toBeUndefined();
  });
});

describe("defineFlowPanel v2 — with resources (object form)", () => {
  it("resolves resources from object config", () => {
    const mockAdapter = createMockResourceAdapter();

    const fp = defineFlowPanel({
      ...baseConfig,
      adapter: { sql: mockSqlExecutor, resource: mockAdapter },
      resources: {
        users: resource("User", { label: "Users", icon: "users" }),
        posts: resource("Post", { label: "Posts" }),
      },
    });

    expect(fp.resources).toBeDefined();
    expect(Object.keys(fp.resources!)).toEqual(["users", "posts"]);
    expect(fp.resourceAdapter).toBe(mockAdapter);

    // Check resolved user resource
    const userRes = fp.resources!.users;
    expect(userRes.id).toBe("users");
    expect(userRes.modelName).toBe("User");
    expect(userRes.label).toBe("Users");
    expect(userRes.icon).toBe("users");

    // Check resolved post resource
    const postRes = fp.resources!.posts;
    expect(postRes.id).toBe("posts");
    expect(postRes.modelName).toBe("Post");
    expect(postRes.label).toBe("Posts");
  });

  it("auto-generates columns from metadata", () => {
    const mockAdapter = createMockResourceAdapter();

    const fp = defineFlowPanel({
      ...baseConfig,
      adapter: { sql: mockSqlExecutor, resource: mockAdapter },
      resources: {
        users: resource("User"),
      },
    });

    const userRes = fp.resources!.users;
    // Should have auto-generated columns for non-autoGenerated fields
    const colIds = userRes.columns.map((c) => c.id);
    expect(colIds).toContain("email");
    expect(colIds).toContain("name");
    expect(colIds).toContain("role");
    // autoGenerated fields should be excluded
    expect(colIds).not.toContain("id");
    expect(colIds).not.toContain("createdAt");
  });
});

