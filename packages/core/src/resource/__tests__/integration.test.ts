describe("defineFlowPanel v2 — resource with actions and computes", () => {
  it("resolves actions and computes into internal maps", () => {
    const mockAdapter = createMockResourceAdapter();
    const banHandler = vi.fn(async () => {});

    const fp = defineFlowPanel({
      ...baseConfig,
      adapter: { sql: mockSqlExecutor, resource: mockAdapter },
      resources: {
        users: resource("User", {
          columns: (c) => [
            c.field((p) => p.email),
            c.computed("displayName", {
              label: "Display Name",
              compute: (row) => String(row.name).toUpperCase(),
            }),
          ],
          actions: (a) => [
            a.mutation({
              label: "Ban User",
              icon: "ban",
              variant: "danger",
              confirm: "Are you sure?",
              when: (row) => row.role !== "admin",
              handler: banHandler,
            }),
          ],
        }),
      },
    });

    const userRes = fp.resources!.users;

    // Computes
    expect(userRes._computes).toBeDefined();
    expect(typeof userRes._computes.displayName).toBe("function");
    expect(userRes._computes.displayName({ name: "alice", email: "a@b.c" })).toBe("ALICE");

    // Actions
    expect(userRes.actions).toHaveLength(1);
    expect(userRes.actions[0].label).toBe("Ban User");
    expect(userRes.actions[0].variant).toBe("danger");

    // Handlers map
    const actionId = userRes.actions[0].id;
    expect(userRes._handlers[actionId]).toBe(banHandler);

    // Whens map
    expect(typeof userRes._whens[actionId]).toBe("function");
    expect(userRes._whens[actionId]({ role: "user" })).toBe(true);
    expect(userRes._whens[actionId]({ role: "admin" })).toBe(false);
  });
});

