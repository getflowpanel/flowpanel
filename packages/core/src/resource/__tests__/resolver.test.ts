describe("inferIncludeFromPaths", () => {
  it("scalar path produces empty include", () => {
    expect(inferIncludeFromPaths(["email"])).toEqual({});
  });

  it("single relation path produces include true", () => {
    expect(inferIncludeFromPaths(["user.email"])).toEqual({ user: true });
  });

  it("nested relation path produces nested include", () => {
    expect(inferIncludeFromPaths(["user.subscription.planId"])).toEqual({
      user: { include: { subscription: true } },
    });
  });

  it("_count paths are skipped", () => {
    expect(inferIncludeFromPaths(["_count.posts"])).toEqual({});
  });

  it("merges duplicate relations", () => {
    const result = inferIncludeFromPaths(["user.email", "user.name"]);
    expect(result).toEqual({ user: true });
  });

  it("merges nested and flat for same relation", () => {
    const result = inferIncludeFromPaths(["user.email", "user.subscription.planId"]);
    expect(result).toEqual({ user: { include: { subscription: true } } });
  });

  it("handles multiple different relations", () => {
    const result = inferIncludeFromPaths(["user.email", "plan.name"]);
    expect(result).toEqual({ user: true, plan: true });
  });
});

