describe("resolveResource — actions", () => {
  it("resolves actions and sets id from builder return", () => {
    const descriptor = createResourceDescriptor<{ email: string }>("Subscription", {
      actions: (a) => {
        const cancel = a.mutation({
          label: "Cancel",
          handler: async () => {},
        });
        cancel.id = "cancel";
        return [cancel];
      },
    });
    const resolved = resolveResource("subscription", descriptor, mockMetadata);
    expect(resolved.actions[0].id).toBe("cancel");
    expect(resolved.actions[0].label).toBe("Cancel");
  });

  it("builds _handlers map from actions", () => {
    const handler = async () => {};
    const descriptor = createResourceDescriptor<{ email: string }>("Subscription", {
      actions: (a) => {
        const act = a.mutation({ label: "Do it", handler });
        act.id = "doIt";
        return [act];
      },
    });
    const resolved = resolveResource("subscription", descriptor, mockMetadata);
    expect(typeof resolved._handlers["doIt"]).toBe("function");
  });

  it("builds _whens map from actions with when predicate", () => {
    const when = (row: any) => row.status === "active";
    const descriptor = createResourceDescriptor<{ status: string }>("Subscription", {
      actions: (a) => {
        const act = a.mutation({ label: "Act", handler: async () => {}, when });
        act.id = "act";
        return [act];
      },
    });
    const resolved = resolveResource("subscription", descriptor, mockMetadata);
    expect(typeof resolved._whens["act"]).toBe("function");
  });

  it("throws on reserved action ID 'delete'", () => {
    const descriptor = createResourceDescriptor<{ email: string }>("Subscription", {
      actions: (a) => {
        const act = a.mutation({ label: "Delete", handler: async () => {} });
        act.id = "delete";
        return [act];
      },
    });
    expect(() => resolveResource("subscription", descriptor, mockMetadata)).toThrow(/reserved/i);
  });

  it("throws on reserved action ID 'list'", () => {
    const descriptor = createResourceDescriptor<{ email: string }>("Subscription", {
      actions: (a) => {
        const act = a.mutation({ label: "List", handler: async () => {} });
        act.id = "list";
        return [act];
      },
    });
    expect(() => resolveResource("subscription", descriptor, mockMetadata)).toThrow(/reserved/i);
  });

  it("throws on reserved action ID 'create'", () => {
    const descriptor = createResourceDescriptor<{ email: string }>("Subscription", {
      actions: (a) => {
        const act = a.mutation({ label: "Create", handler: async () => {} });
        act.id = "create";
        return [act];
      },
    });
    expect(() => resolveResource("subscription", descriptor, mockMetadata)).toThrow(/reserved/i);
  });
});

