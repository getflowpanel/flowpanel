describe("mergeIncludes", () => {
  it("returns inferred when manual is empty", () => {
    const inferred = { user: true };
    expect(mergeIncludes(inferred, {})).toEqual({ user: true });
  });

  it("manual wins on conflict", () => {
    const inferred = { user: true };
    const manual = { user: { include: { posts: true } } };
    const result = mergeIncludes(inferred, manual);
    expect(result).toEqual({ user: { include: { posts: true } } });
  });

  it("merges non-conflicting keys", () => {
    const inferred = { user: true };
    const manual = { plan: true };
    expect(mergeIncludes(inferred, manual)).toEqual({ user: true, plan: true });
  });

  it("manual undefined produces inferred", () => {
    const inferred = { user: true };
    expect(mergeIncludes(inferred, undefined)).toEqual({ user: true });
  });
});
